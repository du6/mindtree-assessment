package main.java.mindtree.form;

/**
 * Pojo representing a Edge form on the client side.
 */

public class EdgeForm {
  /**
   * The parent key
   */
  private String parentKey;

  /**
   * The child key
   */
  private String childKey;

  public String getParentKey() {
    return parentKey;
  }

  public String getChildKey() {
    return childKey;
  }
}
